<p><strong>Sarana Alternatif Penyelesaian Pengaduan</strong></p>

<p><img src="Penanganan-Pengaduan-Nasabah-BSM-3.png"></p>

<ol>
    <li>Dalam hal tidak tercapai kesepakatan penyeresaian pengaduan, nasabah dapat melakukan penyelesaian sengketa di luar atau melalui pengadilan.</li>
    <li>Penyelesaian sengketa di luar jalur pengadilan agama sebagaimana dimaksud pada huruf 1 di atas dapat dilakukan melalui: Lembaga Alternatif Penyelesaian Sengketa Perbankan Indonesia (LAPSPI), Mediasi Perbankan atau fasilitasi terbatas oleh Otoritas Jasa Keuangan.</li>
    <li>Dalam hal penyelesaian sengketa tidak dilakukan melalui Lembaga Alternatif Penyelesaian Sengketa atau Mediasi Perbankan sebagaimana dimaksud pada poin 2, nasabah dapat menyampaikan permohonan kepada regulator untuk memfasilitasi penyelesaian pengaduan nasabah yang dirugikan oleh Bank.</li>
    <li>Pemberian fasilitas penyelesaian pengaduan nasabah oleh regulator dilakukan terhadap pengaduan yang berindikasi sengketa di sektor jasa keuangan harus memenuhi persyaratan sebagai berikut:
        <ol type="a">
            <li>Kerugian finansial yang dialami nasabah adalah maksimal sebesar Rp500.000.000,- (lima ratus juta rupiah).</li>
            <li>Permohonan tertulis dari nasabah disertai dengan dokumen pendukung terkait dengan pengaduan yang disampaikan.</li>
            <li>Bank telah melakukan upaya penyelesaian pengaduan namun nasabah tidak dapat menerima penyelesaian tersebut atau telah melewali batas waktu yang ditetapkan.</li>
            <li>Pengaduan yang diajukan bukan merupakan sengketa yang sedang dalam proses dan/atau pernah diputus oleh lembaga arbitrase atau peradilan, atau lembaga mediasi lain.</li>
            <li>Pengaduan yang diajukan adalah pengaduan perdata.</li>
            <li>Pengaduan yang diajukan belum pernah difasilitasi oleh regulator.</li>
            <li>Pengajuan penyelesaian pengaduan tidak melebihi 60 (enam puluh) hari kerja sejak tanggal surat hasil penyelesaian pengaduan yang disampaikan Bank kepada nasabah.</li>
        </ol>
    </li>
    <li>Dalam hal kerugian yang dialami nasabah melebihi Rp500.000.000,- (lima ratus juta rupiah) maka penyelesaian pengaduan dapat melalui lembaga peradilan yang disepakati.</li>
    <li>Proses Fasilitasi Penyelesaian Pengaduan Nasabah melalui Lembaga Alternatif Penyelesaian Sengketa atau Mediasi Perbankan
        <ol type="a">
            <li>Dalam proses penyelesaian pengaduan nasabah, regulator akan menunjuk fasilitator untuk melaksanakan fungsi penyelesaian pengaduan.</li>
            <li>Proses fasilitasi penyelesaian pengaduan dimulai setelah nasabah dan Bank sepakat untuk difasilitasi oleh regulator yang dituangkan dalam perjanjian fasilitas yang memuat:
                <ol>
                    <li>Kesepakatan untuk memilih penyelesaian pengaduan yang difasilitasi oleh regulator.</li>
                    <li>Persetujuan untuk tunduk dan patuh pada aturan fasilitasi yang ditetapkan oleh regulator.</li>
                    <li>Pelaksanaan proses fasilitasi sampai dengan ditandatanganinya akta kesepakatan dilakukan dalam jangka waktu paling lama 30 (tiga puluh) hari kerja sejak nasabah dan Bank menandatangani perjanjian sebagaimana dimaksud dalam poin 6.b.2</li>
                </ol>
            </li>
        </ol>
    </li>
    <li>Jangka waktu proses fasilitasi sebagaimana dimaksud pada poin 6.b.3 dapat diperpanjang sampai dengan 30 (tiga puluh) hari kerja berikutnya berdasarkan akta kesepakatan nasabah dan Bank.</li>
