                    <li>Keylogger yaitu suatu aplikasi atau <em>software</em> yang dapat mengunci tombol<em> keyboard</em> dengan menggunakan program <em>logger</em> tertentu. Sehingga, apapun yang diketikkan oleh user di layar monitor, dapat terekam. Artinya, meskipun saat mengetikkan password di kotak password yang tampil di monitor hanyalah ‘*****’ misalnya, namun isi password tersebut dapat terekam dan otomatis dapat terbaca. Hasil rekaman ini akan langsung tersimpan pada komputer dan dikirimkan melalui internet kepada si pencuri data tersebut. Bisa lewat e-mail, irc atau bahkan bisa diamati langsung secara realtime melalui web.<br>
