            <li><strong>Transaksi menggunakan kartu pada mesin ATM</strong>
                <ol>
                    <li><em>Trapping</em>, yaitu membuat kartu ATM yang dimasukan ke dalam <em>card reader</em> pada mesin ATM pada saat transaksi sehingga kartu ATM menjadi tersangkut.<br>
                        Cara kerja <em>card trapping</em> adalah pelaku dengan menggunakan pita film, batang korek api, pita rambut, <em>double tape</em>. Di Indonesia mayoritas pelaku menggunakan batang korek api karena harga yang murah dan sangat cepat dilakukan. Ciri-ciri mesin ATM yang telah terpasang alat <em>card trapping</em> adalah pada saat transaksi maka kartu ATM akan tersangkut dan kartu ATM tidak akan pernah bisa diproses untuk transaksi.</li>
                    <li><em>Skimming, </em>yaitu menggandakan data kartu ATM nasabah pada saat transaksi di mesin ATM yang telah dipasang alat <em>skimmer</em> di depan mulut <em>card reader.</em><br>
                        Cara kerja <em>card skimming</em> adalah pelaku dengan mengunakan alat <em>skimmer</em> (pengganda data kartu ATM) yang dirancang oleh pelaku sehingga menyerupai bentuk dari <em>card reader </em>asli mesin ATM. Alat <em>skimmer</em> dapat terdeteksi dengan mudah oleh yaitu:
                        <p>- Bentuk dan warna akan sangat berbeda apabila dilihat dari bentuk mulut <em>card reader</em> mesin ATM yang ada.<br>
                        <br>
                        - Alat <em>skimmer</em> hanya direkatkan dengan menggunakan <em>double tape</em> karena prinispnya para pelaku akan mengambilnya kembali dalam hitungan detik karena data yang telah di capture akan segera di ambil dari alat tersebut.<br>
                        Data kartu ATM yang telah digandakan tersebut tidak akan berfungsi apabila tidak adanya PIN (<em>Personal Indetification Number</em>), pelaku akan menambahkan <em>camera</em> yang berukuran kecil sekali untuk melihat PIN yang nasabah inputkan.</p>
                    </li>
                    <li><em>Phising</em>, yaitu pencurian data melalui web site palsu atau tindakan memperoleh informasi pribadi seperti <em>User ID </em>&amp; <em>Password</em>, PIN, nomor rekening bank secara tidak sah. Informasi ini kemudian akan dimanfaatkan oleh pihak penipu untuk mengakses rekening, melakukan penipuan atau memandu nasabah untuk melakukan transfer ke rekening tertentu dengan iming-iming hadiah.</li>
                </ol>
